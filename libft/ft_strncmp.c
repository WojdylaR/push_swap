#include "libft.h"

int	ft_strncmp(char const *str1, char const *str2, size_t length)
{
	unsigned int	i;
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)str1;
	s2 = (unsigned char *)str2;
	i = 0;
	if (length == 0)
		return (0);
	while (length - 1 > i && s1[i] && s2[i])
	{
		if (str1[i] != str2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}
